package project2;

public class Task7 {
    public static void main(String[] args) {

        double radius = 5;
        double Pi = 3.14;

        double circumferenceRadius = 2 * Pi * radius;
        double areaRadius = Pi * radius * radius;

        System.out.println("For a circle with radius = " + radius + ":");
        System.out.println("circumference with radius = " + circumferenceRadius);
        System.out.println("Area with radius = " + areaRadius);

    }
}
